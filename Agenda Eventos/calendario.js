// Función para generar el calendario
function generarCalendario() {
  const container = document.getElementById("contenido-calendario");
  const mesTitulo = document.getElementById("mes-actual");
  const daysInMonth = new Date(anioActual, mesActual + 1, 0).getDate(); // Días del mes actual
  const firstDay = new Date(anioActual, mesActual, 1).getDay(); // Día de la semana del primer día del mes

  // Mostrar el nombre del mes y el año
  mesTitulo.textContent = `${meses[mesActual]} ${anioActual}`;

  // Limpiar contenido previo del contenedor
  container.innerHTML = "";

  // Crear días vacíos al inicio del calendario para alinearlo correctamente
  for (let i = 0; i < firstDay; i++) {
    const emptyDiv = document.createElement("div");
    emptyDiv.classList.add("empty");
    container.appendChild(emptyDiv);
  }

  // Crear los días del mes
  for (let day = 1; day <= daysInMonth; day++) {
    const currentDate = `${anioActual}-${String(mesActual + 1).padStart(
      2,
      "0"
    )}-${String(day).padStart(2, "0")}`;
    const dayDiv = document.createElement("div");
    dayDiv.textContent = day;

    // Si el día es una fiesta, resaltar
    if (todasFestividades[currentDate]) {
      dayDiv.classList.add("highlight");

      //Obtener las festividades del dia
      const festividades = Array.isArray(todasFestividades[currentDate])
        ? todasFestividades[currentDate]
        : [todasFestividades[currentDate]];

      // Mostrar todas las festividades en el título
      dayDiv.title = festividades.join("\n");

      // Crear una lista para los colores
      let colores = [];

      // Verificar cada conjunto de festividades y agregar sus colores
      if (festividadesCR.fechas[currentDate]) {
        colores.push(festividadesCR.color);
      }
      if (festividadesCLM.fechas[currentDate]) {
        colores.push(festividadesCLM.color);
      }
      if (festividadesToledo.fechas[currentDate]) {
        colores.push(festividadesToledo.color);
      }
      if (festividadesAlbacete.fechas[currentDate]) {
        colores.push(festividadesAlbacete.color);
      }
      if (festividadesCuenca.fechas[currentDate]) {
        colores.push(festividadesCuenca.color);
      }
      if (festividadesGuadalajara.fechas[currentDate]) {
        colores.push(festividadesGuadalajara.color);
      }

      // Aplicar un gradiente con los colores
      if (colores.length > 1) {
        dayDiv.style.background = `repeating-linear-gradient(90deg, ${colores
          .map(
            (color, i) =>
              `${color} ${i * (100 / colores.length)}%, ${color} ${
                (i + 1) * (100 / colores.length)
              }%`
          )
          .join(", ")})`;
      } else if (colores.length === 1) {
        dayDiv.style.backgroundColor = colores[0];
      }

      // Añadir evento de clic para mostrar la fiesta
      dayDiv.addEventListener("click", () => {
        // Generar un array con todas las festividades del día
        const todasFestividadesDia = [];

        // Verificar y añadir las festividades de cada región
        if (festividadesCR.fechas[currentDate]) {
          todasFestividadesDia.push(
            ...[].concat(festividadesCR.fechas[currentDate])
          );
        }
        if (festividadesCLM.fechas[currentDate]) {
          todasFestividadesDia.push(
            ...[].concat(festividadesCLM.fechas[currentDate])
          );
        }
        if (festividadesToledo.fechas[currentDate]) {
          todasFestividadesDia.push(
            ...[].concat(festividadesToledo.fechas[currentDate])
          );
        }
        if (festividadesAlbacete.fechas[currentDate]) {
          todasFestividadesDia.push(
            ...[].concat(festividadesAlbacete.fechas[currentDate])
          );
        }
        if (festividadesCuenca.fechas[currentDate]) {
          todasFestividadesDia.push(
            ...[].concat(festividadesCuenca.fechas[currentDate])
          );
        }
        if (festividadesGuadalajara.fechas[currentDate]) {
          todasFestividadesDia.push(
            ...[].concat(festividadesGuadalajara.fechas[currentDate])
          );
        }

        // Mostrar todas las festividades en un único alert
        mostrarAlerta(
          `El ${day} de ${
            meses[mesActual]
          } tiene las siguientes festividades:\n- ${todasFestividadesDia.join(
            "\n- "
          )}`
        );
      });
    }

    container.appendChild(dayDiv);
  }
}

 // Función para mostrar una alerta personalizada
 function mostrarAlerta(mensaje) {
  document.getElementById("alertMessage").innerText = mensaje; // Cambia el mensaje
  document.getElementById("customAlert").style.display = "block"; // Muestra la alerta
  document.getElementById("customOverlay").style.display = "block"; 
}

// Función para cerrar la alerta
function cerrarAlerta() {
  document.getElementById("customAlert").style.display = "none"; // Oculta la alerta
  document.getElementById("customOverlay").style.display = "none";
}

// Función para cambiar el mes
function cambiarMes(direccion) {
  mesActual += direccion;

  if (mesActual < 0) {
    mesActual = 11; // Diciembre
    anioActual--;
  } else if (mesActual > 11) {
    mesActual = 0; // Enero
    anioActual++;
  }

  generarCalendario();
}

// Botones de navegación
document
  .getElementById("prev-mes")
  .addEventListener("click", () => cambiarMes(-1));
document
  .getElementById("next-mes")
  .addEventListener("click", () => cambiarMes(1));

// Generar el calendario al cargar la página
document.addEventListener("DOMContentLoaded", generarCalendario);
